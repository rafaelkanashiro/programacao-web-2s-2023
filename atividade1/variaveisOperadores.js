// Declaração de variáveis numéricas
var numA = 10;
var numB = 5;

// Realize operações e armazene os resultados em variáveis separadas
var resultadoSoma = numA + numB;
var resultadoSubtracao = numA - numB;
var resultadoMultiplicacao = numA * numB;
var resultadoDivisao = numA / numB;

// Imprima os resultados com o formato especificado
console.log(`Resultado da soma entre ${numA} e ${numB} = ${resultadoSoma}`);
console.log(`Resultado da subtração entre ${numA} e ${numB} = ${resultadoSubtracao}`);
console.log(`Resultado da multiplicação entre ${numA} e ${numB} = ${resultadoMultiplicacao}`);
console.log(`Resultado da divisão entre ${numA} e ${numB} = ${resultadoDivisao}`);
