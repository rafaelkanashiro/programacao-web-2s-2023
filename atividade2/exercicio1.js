// Declaração de duas variáveis numéricas
var numA = 10;
var numB = 5;

// Realize a operação de adição e armazene o resultado em uma terceira variável
var numC_Add = numA + numB;
console.log(`Resultado da adição entre ${numA} e ${numB} = ${numC_Add}`);

// Realize a operação de subtração e armazene o resultado em uma terceira variável
var numC_Subtract = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} = ${numC_Subtract}`);

// Realize a operação de multiplicação e armazene o resultado em uma terceira variável
var numC_Multiply = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} = ${numC_Multiply}`);

// Realize a operação de divisão e armazene o resultado em uma terceira variável
var numC_Divide = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} = ${numC_Divide}`);
